#!/usr/bin/env bash
#
# build/bundle.sh — concatenate lib/ + adapters/ + bin/loadout-pipeline.sh
# into a single self-contained, minified dist/loadout-pipeline.sh.
#
# The bundle is a BusyBox-style multi-call script: the forked sub-stages
# (extract, dispatch, precheck, and the five adapters) re-invoke the bundle
# itself with a sentinel first argument so real PIDs are still created —
# the worker registry, orphan recovery, and SIGKILL handling all keep
# working bit-for-bit.
#
# Usage: bash build/bundle.sh
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
DIST_DIR="$ROOT_DIR/dist"
OUTPUT="$DIST_DIR/loadout-pipeline.sh"

mkdir -p "$DIST_DIR"
: > "$OUTPUT"

# ─── call-site rewrite via sed ───────────────────────────────────────────────
# Every forked sub-process gets one entry. The left side is the literal
# substring that appears in the source file; the right side re-invokes the
# bundle with `__lp_mode <name>`. Credential-isolation wrappers of the form
# `env "${_strip_args[@]}" bash …` still work because only the `bash <path>`
# part is rewritten — the env prefix is untouched.
_rewrite_calls() {
    sed \
        -e 's|bash "\$ROOT_DIR/lib/extract\.sh"|bash "$__LP_BUNDLE_PATH" __lp_mode extract|g' \
        -e 's|bash "\$ROOT_DIR/lib/dispatch\.sh"|bash "$__LP_BUNDLE_PATH" __lp_mode dispatch|g' \
        -e 's|bash "\$ROOT_DIR/lib/precheck\.sh"|bash "$__LP_BUNDLE_PATH" __lp_mode precheck|g' \
        -e 's|bash "\$ROOT_DIR/adapters/ftp\.sh"|bash "$__LP_BUNDLE_PATH" __lp_mode adapter_ftp|g' \
        -e 's|bash "\$ROOT_DIR/adapters/hdl_dump\.sh"|bash "$__LP_BUNDLE_PATH" __lp_mode adapter_hdl_dump|g' \
        -e 's|bash "\$ROOT_DIR/adapters/sdcard\.sh"|bash "$__LP_BUNDLE_PATH" __lp_mode adapter_sdcard|g' \
        -e 's|bash "\$ROOT_DIR/adapters/rclone\.sh"|bash "$__LP_BUNDLE_PATH" __lp_mode adapter_rclone|g' \
        -e 's|bash "\$ROOT_DIR/adapters/rsync\.sh"|bash "$__LP_BUNDLE_PATH" __lp_mode adapter_rsync|g'
}

# ─── minifier ────────────────────────────────────────────────────────────────
# Three passes, applied to every inlined file:
#   1. drop the shebang line (bundle has its own at the top)
#   2. drop full-line comments  (first non-whitespace char is '#')
#   3. drop blank lines and strip trailing whitespace
#
# Leading whitespace is *preserved* — stripping it would corrupt the middle
# lines of any multi-line quoted string. Bash doesn't care about indentation
# so the savings would be cosmetic anyway; correctness matters more.
_minify() {
    awk '
        NR == 1 && /^#!/ { next }
        /^[[:space:]]*#/ { next }
        /^[[:space:]]*$/ { next }
        {
            sub(/[[:space:]]+$/, "")
            print
        }
    '
}

# ─── per-file pre-processing ─────────────────────────────────────────────────
# Each source file has its own `ROOT_DIR=…` guard and several `source …`
# statements that resolve sibling files. In the bundle there are no sibling
# files (everything is already inlined) and ROOT_DIR is set once at the top,
# so both patterns must be deleted from every inlined file before the
# minifier runs. Order: strip → rewrite calls → minify.
_preprocess() {
    sed \
        -e '/^[[:space:]]*ROOT_DIR=.*BASH_SOURCE/d' \
        -e '/^[[:space:]]*source[[:space:]]\+"\$ROOT_DIR\//d' \
        -e '/^[[:space:]]*export[[:space:]]\+ROOT_DIR[[:space:]]*$/d' \
    | _rewrite_calls \
    | _minify
}

# ─── emit_header ─────────────────────────────────────────────────────────────
emit_header() {
    cat >> "$OUTPUT" <<'HEADER'
#!/usr/bin/env bash
# loadout-pipeline — bundled single-file distribution. Generated by
# build/bundle.sh from lib/, adapters/, and bin/loadout-pipeline.sh. Do not
# edit this file directly; re-run the bundler instead.
set -euo pipefail
__LP_BUNDLE_PATH="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)/$(basename "${BASH_SOURCE[0]}")"
export __LP_BUNDLE_PATH
ROOT_DIR="$(dirname "$__LP_BUNDLE_PATH")"
export ROOT_DIR
HEADER
}

# ─── emit_strip_list_setup ───────────────────────────────────────────────────
# At top-level invocation, the bundle materialises the embedded strip.list
# into a tmp file and points EXTRACT_STRIP_LIST at it. Every downstream reader
# — extract.sh:_strip_pass, lib/precheck.sh, lib/strip_list.sh, and the
# resume planner — honours EXTRACT_STRIP_LIST, so there is exactly one strip
# path and it is live for every stage without any function patching.
#
# Forked sub-stages (__lp_mode …) inherit the already-set EXTRACT_STRIP_LIST
# via their exported environment, so the tmp file is only written once per
# top-level run. A user who has set EXTRACT_STRIP_LIST themselves is never
# overridden. A user who wants to disable stripping entirely can still set
# EXTRACT_STRIP_LIST="" and the test in _strip_pass skips everything.
emit_strip_list_setup() {
    {
        echo 'if [[ "${1:-}" != "__lp_mode" && -z "${EXTRACT_STRIP_LIST+x}" ]]; then'
        echo '    __lp_strip_tmp="$(mktemp -t loadout-pipeline-strip.XXXXXX)"'
        echo "    trap 'rm -f \"\$__lp_strip_tmp\" 2>/dev/null || true' EXIT"
        echo "    cat > \"\$__lp_strip_tmp\" <<'LP_STRIPLIST_EOF'"
        if [[ -f "$ROOT_DIR/strip.list" ]]; then
            cat "$ROOT_DIR/strip.list"
        fi
        echo 'LP_STRIPLIST_EOF'
        echo '    export EXTRACT_STRIP_LIST="$__lp_strip_tmp"'
        echo 'fi'
    } >> "$OUTPUT"
}

# ─── inline_sourced_file ─────────────────────────────────────────────────────
# Inlines a file that was originally loaded via `source`. Definitions land in
# the top-level shell and become visible to every subsequent inlined file.
inline_sourced_file() {
    local path="$1"
    echo "# ── inlined from $path ─────────────────────────────────────────" >> "$OUTPUT"
    _preprocess < "$ROOT_DIR/$path" >> "$OUTPUT"
}

# ─── inline_forked_file ──────────────────────────────────────────────────────
# Wraps a file that was originally forked (`bash lib/foo.sh …`) as a function
# named __lp_main_<name>. The function body is a plain brace group, NOT a
# subshell: the forked bundle-bash process IS the worker, and a `( … )`
# wrapper would add a spurious extra PID between unzip_worker and the
# inlined extract.sh code. Test 18 (registry-kill orphan recovery) detects
# that extra layer because its 7z shim walks $PPID→grandparent expecting to
# land on unzip_worker, and an extra subshell shifts the grandparent to the
# wrong process. `set -euo pipefail` and EXIT traps inside the body are
# scoped to the subprocess (which is the whole forked bash), which is
# exactly what the forked stages expect in their original standalone form.
inline_forked_file() {
    local path="$1" funcname="$2"
    echo "# ── inlined forked ── $path → $funcname ──" >> "$OUTPUT"
    echo "$funcname() {" >> "$OUTPUT"
    _preprocess < "$ROOT_DIR/$path" >> "$OUTPUT"
    echo "}" >> "$OUTPUT"
}

# ─── emit_dispatcher ─────────────────────────────────────────────────────────
# BusyBox-style mode router. Consumes the sentinel first argument if present,
# dispatches to the matching __lp_main_* function, and exits with its return
# code. When the sentinel is absent, control falls through to the entry-point
# body (inlined last) and the pipeline runs normally.
emit_dispatcher() {
    cat >> "$OUTPUT" <<'DISPATCH'
# ── mode dispatcher ────────────────────────────────────────────────────────
if [[ "${1:-}" == "__lp_mode" ]]; then
    __lp_submode="$2"; shift 2
    case "$__lp_submode" in
        extract)          __lp_main_extract          "$@"; exit $? ;;
        dispatch)         __lp_main_dispatch         "$@"; exit $? ;;
        precheck)         __lp_main_precheck         "$@"; exit $? ;;
        adapter_ftp)      __lp_main_adapter_ftp      "$@"; exit $? ;;
        adapter_hdl_dump) __lp_main_adapter_hdl_dump "$@"; exit $? ;;
        adapter_sdcard)   __lp_main_adapter_sdcard   "$@"; exit $? ;;
        adapter_rclone)   __lp_main_adapter_rclone   "$@"; exit $? ;;
        adapter_rsync)    __lp_main_adapter_rsync    "$@"; exit $? ;;
        *) echo "loadout-pipeline: unknown internal mode '$__lp_submode'" >&2; exit 2 ;;
    esac
fi
DISPATCH
}


# ─── main build sequence ─────────────────────────────────────────────────────
emit_header
emit_strip_list_setup

# Sourced files in dependency order. config.sh reads .env (references
# ROOT_DIR); logging.sh is used by everything below; job_format.sh is used
# by jobs/workers/resume_planner; jobs.sh and queue.sh are standalone;
# space.sh and worker_registry.sh are used by workers.sh; resume_planner.sh
# must come before workers.sh (which sources it); workers.sh is last among
# the sourced set because it wires everything together.
for f in \
    lib/config.sh \
    lib/logging.sh \
    lib/init.sh \
    lib/job_format.sh \
    lib/strip_list.sh \
    lib/jobs.sh \
    lib/queue.sh \
    lib/space.sh \
    lib/worker_registry.sh \
    lib/resume_planner.sh \
    lib/workers.sh
do
    inline_sourced_file "$f"
done

# Forked files → __lp_main_* functions. These define, but do not run. The
# dispatcher below routes to them on re-invocation.
inline_forked_file lib/extract.sh         __lp_main_extract
inline_forked_file lib/dispatch.sh        __lp_main_dispatch
inline_forked_file lib/precheck.sh        __lp_main_precheck
inline_forked_file adapters/ftp.sh        __lp_main_adapter_ftp
inline_forked_file adapters/hdl_dump.sh   __lp_main_adapter_hdl_dump
inline_forked_file adapters/sdcard.sh     __lp_main_adapter_sdcard
inline_forked_file adapters/rclone.sh     __lp_main_adapter_rclone
inline_forked_file adapters/rsync.sh      __lp_main_adapter_rsync

# Dispatcher must come AFTER every __lp_main_* function is defined and
# BEFORE the entry-point body that would otherwise start a pipeline run.
emit_dispatcher

# Entry-point body runs last. _preprocess strips its `source …` lines so
# nothing collides with the already-inlined definitions. Its
# `ROOT_DIR=…/BASH_SOURCE` line is also deleted; ROOT_DIR is set in the
# bundle header.
echo "# ── inlined entry point ── bin/loadout-pipeline.sh ──" >> "$OUTPUT"
_preprocess < "$ROOT_DIR/bin/loadout-pipeline.sh" >> "$OUTPUT"

chmod +x "$OUTPUT"

lines=$(wc -l < "$OUTPUT")
size=$(du -h "$OUTPUT" | cut -f1)
echo "[bundle] wrote $OUTPUT ($lines lines, $size)"

# Syntax sanity check — catch obvious failures before the user runs tests.
if ! bash -n "$OUTPUT"; then
    echo "[bundle] ERROR: bash -n reported a syntax error in $OUTPUT" >&2
    exit 1
fi
echo "[bundle] syntax check OK"
